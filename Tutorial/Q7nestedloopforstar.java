package Tutorial;

public class Q7nestedloopforstar {
    public static void main(String[] args) {

        //star
        for(int i=1;i<=5;i++){//控制每一行
            for(int j = 1;j<=i;j++){//控制每一个行里的element
                System.out.print(j);//很好玩 一定要记得
            }
            System.out.println();//换行
        }

    }
}
            
